package com.bitschool.dto;

public class BookCalendarDTO {

	private String bookDate;
	private String bookTime;
	private String bookEmail;
	private int locationSeq;
	public String getBookDate() {
		return bookDate;
	}
	public void setBookDate(String bookDate) {
		this.bookDate = bookDate;
	}
	public String getBookTime() {
		return bookTime;
	}
	public void setBookTime(String bookTime) {
		this.bookTime = bookTime;
	}
	public String getBookEmail() {
		return bookEmail;
	}
	public void setBookEmail(String bookEmail) {
		this.bookEmail = bookEmail;
	}
	public int getLocationSeq() {
		return locationSeq;
	}
	public void setLocationSeq(int locationSeq) {
		this.locationSeq = locationSeq;
	}
	
	
}
